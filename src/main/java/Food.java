public class Food {
    public String product;
    public String cal;
    public String belok;
    public String shir;
    public String uglevod;

    public Food(){

    }

    public String getProduct(){
        return product;
    }
    public void setProduct(String product){
        this.product = product;
    }

    public String getCal(){
        return cal;
    }
    public void setCal(String cal){
        this.cal = cal;
    }
    public String getBelok(){
        return belok;
    }
    public void setBelok(String belok){
        this.belok = belok;
    }

    public String getShir(){
        return shir;
    }
    public void setShir(String shir){
        this.shir = shir;
    }
    public String getUglevod(){
        return uglevod;
    }
    public void setUglevod(String uglevod){
        this.uglevod = uglevod;
    }
}


